class Library {
  #books;
  constructor(initialBooks = []) {
    // –≤–æ—Ç —Ç—É—Ç –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –º–∞—Å—Å–∏–≤–∞, –µ–≥–æ –º–æ–∂–Ω–æ –¥–µ–ª–∞—Ç—å –ø—Ä—è–º–æ –≤ —Å–∫–æ–±–∫–∞—Ö –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä–∞
    if (new Set(initialBooks).size !== initialBooks.length) {
      throw new Error("–ù–∞—á–∞–ª—å–Ω—ã–π —Å–ø–∏—Å–æ–∫ —Å–æ–¥–µ—Ä–∂–∏—Ç –¥—É–±–ª–∏–∫–∞—Ç—ã");
    }
    this.#books = initialBooks;
  }
  get allBooks() {
    return this.#books;
  }
  addBook(title) {
    if (this.#books.includes(title)) {
      throw new Error(`–ö–Ω–∏–≥–∞ —Å –Ω–∞–∑–≤–∞–Ω–∏–µ–º "${title}" —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç`);
    }
    this.#books.push(title);
  }
  removeBook(title) {
    const bookIndex = this.#books.indexOf(title);
    if (bookIndex === -1) {
      throw new Error(`–ö–Ω–∏–≥–∏ —Å –Ω–∞–∑–≤–∞–Ω–∏–µ–º  "${title}" –Ω–µ—Ç –≤ –±–∏–±–ª–∏–æ—Ç–µ–∫–µ`);
    }
    this.#books.splice(bookIndex, 1);
  }

  hasBook(title) {
    return this.#books.includes(title);
  }
}

